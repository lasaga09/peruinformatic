     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        /*validacion de rol repetido*/
          $rol=Rol::where('rol',$request->nombre)->get();
          if($rol->isEmpty()){
             $datos = new Rol();
              $datos->rol=$request->nombre;
              $datos->descripcion=$request->descripcion;
              $datos->save();
              return '1';
          }else{
              return '0';
         
          }

       


        
    }

